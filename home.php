                سياسة الخصوصية
                شكرًا لك على استخدام Cosmo. ثقتك مهمة بالنسبة لنا ونحن ملتزمون بحماية خصوصية وأمان معلوماتك الشخصية. تساعدنا المعلومات التي شاركتها معنا على تقديم تجربة رائعة مع Cosmo. تصف سياسة الخصوصية هذه ممارسات الخصوصية الخاصة بنا لجميع الخدمات التي نقدمها في جميع أنحاء العالم. يشير مصطلح "Cosmo" أو "نحن" أو "نحن" إلى Cosmo ويشير مصطلح "المستخدم" و "أنت" و "ملكك" إلى نفسك كمستخدم لخدمات Cosmo's سياسة الخصوصية هذه ، إلى جانب الشروط والأحكام ، تحدد القواعد والسياسات العامة التي تحكم استخدامك لمنصتنا. اعتمادًا على أنشطتك عند زيارة منصتنا ، قد يُطلب منك الموافقة على شروط وأحكام إضافية
                
                1. المعلومات التي نجمعها
                1.1 تلتزم Cosmo بالحفاظ على حماية الخصوصية لمستخدميها. تم تصميم سياسة الخصوصية الخاصة بنا لمساعدتك على فهم كيفية قيامنا بجمع واستخدام وحماية المعلومات التي تزودنا بها. عندما تزور Cosmo ، نقوم تلقائيًا بجمع معلومات معينة حول جهازك ، بما في ذلك معلومات حول متصفح الويب وعنوان IP والمنطقة الزمنية وبعض ملفات تعريف الارتباط المثبتة على جهازك. بالإضافة إلى ذلك ، أثناء تصفحك لموقع Cosmo على الويب ، نجمع معلومات حول صفحات الويب الفردية أو الأطباء الذين تشاهدهم ، ومواقع الويب أو مصطلحات البحث التي أحالتك إلى Cosmo ، ومعلومات حول كيفية تفاعلك مع Cosmo. نستخدم هذه المعلومات المهمة لسبب محدد لمساعدتنا في فحص المخاطر المحتملة والاحتيال (على وجه الخصوص ، عنوان IP الخاص بك) ، وتحسين Cosmo وتحسينها (على سبيل المثال ، من خلال إنشاء تحليلات حول كيفية تصفح عملائنا للموقع أو التطبيق والتفاعل معهم ، ولتقييم نجاح حملاتنا التسويقية والإعلانية). سيتم حفظ المعلومات الشخصية للمستخدم لفترة محدودة فقط أثناء استفادتك من استخدام موقع Cosmo على الويب وسيتم حذفها في أي وقت بناءً على طلبك عن طريق مراسلتنا عبر البريد الإلكتروني على customercare@Cosmo.com
                
                1.2 نقوم أيضًا بجمع معلومات منك عند التسجيل على موقع Vezeeta أو حجز موعد مع الطبيب أو الاشتراك في النشرة الإخبارية. عند الحجز أو التسجيل على موقعنا الإلكتروني ، حسب الاقتضاء ، قد يُطلب منك إدخال اسمك أو عنوان بريدك الإلكتروني أو عنوانك البريدي أو رقم هاتفك. نستخدم المعلومات الشخصية التي نجمعها بشكل عام للوفاء بأي حجوزات تم إجراؤها من خلال فيزيتا ، للتواصل معك ، والتحقق من المخاطر المحتملة أو الاحتيال ؛ وعندما يتوافق مع التفضيلات التي شاركتها معنا ، نزودك بالمعلومات أو الإعلانات المتعلقة بمنتجاتنا أو خدماتنا.
                
