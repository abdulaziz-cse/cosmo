                2.1 نحن نسعى جاهدين لحماية معلوماتك باستخدام معايير الحماية الدولية وتنفيذ مجموعة متنوعة من التدابير الأمنية للحفاظ على سلامة معلوماتك الشخصية عند إجراء حجز أو إدخال معلوماتك الشخصية أو إرسالها أو الوصول إليها.
                
