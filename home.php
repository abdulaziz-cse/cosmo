                3.2 إذا كنت تفضل ذلك ، يمكنك اختيار أن يقوم جهاز الكمبيوتر الخاص بك بتحذيرك في كل مرة يتم فيها إرسال ملف تعريف الارتباط ، أو يمكنك اختيار إيقاف تشغيل جميع ملفات تعريف الارتباط عبر إعدادات المتصفح. مثل معظم مواقع الويب ، إذا قمت بإيقاف تشغيل ملفات تعريف الارتباط ، فقد لا تعمل بعض خدماتنا بشكل صحيح.
                
